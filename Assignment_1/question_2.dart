// Take two variables and store age then using if/else condition to determine
// oldest and youngest among them.
void main() {
  var age1 = 18;
  var age2 = 18;
  if (age1 > age2) {
    print("age1 is youngest then age2");
  } else if (age1 < age2) {
    print("age1 is older than age2");
  } else {
    print("age1 and age2 is equal");
  }
}
